let lo = 3=='3' // comparador simples,por que so compara o valor e não o tipo
console.log(lo)

let log = 3===3 // comparador mais especifico pois vai comparar se o dado e do mesmo tipo
console.log(log)

let des = 6 != 6 // operador de desigualdade ou (podendo ler como diferente)
console.log(des)

let desi = 6 !== '6' // operador de disigualdade mais restrito e melhor de se usar para não da erro no codigo
console.log(desi)

let re = 6 < 5  //(<,<=,>,>=) operadore relacionais
